async function parseCSV(){
  await axios.get(
    'https://haigfras-api.herokuapp.com/csv?filename=HF2012_other_data&columns=active:False,local_data_type:Marker-COG'
  ).then((photos: any) => {
    return (
      [{
        layerClass: 'Seabed Images',
        layerNames: {
          2012: {
            data_type: 'Photo',
            photos: photos.data
          }
        }
      }]
    )
  })
}

